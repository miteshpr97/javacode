package precource;

public class loops {
	public static void main(String[] args) {
	
		for(int i=1; i<=30; i=i+1) {
			System.out.println(i);
		}

		int j=1;
		while(j<=20) {
			System.out.println(j);
			j=j+1;
		}
	
		int k=1;
		do {
			System.out.println(k);
			k=k+1;
		}
			while(k<=10);
			
	}
}
