package precource;

public class operators {

	 public static void main(String[] args) {    
	        int p = 25;    
	        int q = 15;    
	        int res = p + q;    
	        System.out.println("p + q = " + res);    
	        res = p - q;    
	        System.out.println("p - q = " + res);    
	        res = p * q;    
	        System.out.println("p * q = " + res);    
	        res = p / q;    
	        System.out.println("p / q = " + res);    
	        res = p % 2;    
	        System.out.println("p % 2 = " + res);    
	    }  
}
